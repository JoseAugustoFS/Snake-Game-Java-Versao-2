package engine.graphics;

public class PedidoImagem {

	public Imagem imagem;
	public int profundidadeZ, deslocamentoX, deslocamentoY;

	
	public PedidoImagem(Imagem imagem, int profundidadeZ, int deslocamentoX, int deslocamentoY)
	{
		this.imagem=imagem;
		this.profundidadeZ=profundidadeZ;
		this.deslocamentoX=deslocamentoX;
		this.deslocamentoY=deslocamentoY;
	}
	
}
